        <h2 class="znu-disc-h2"> A Brief History of ZNU </h2>
        <p>
          ZNU (the Ex Higher School for Agriculture & Animal Science) started 
          its activities in 1975, on a formal basis. In December 1975, as 
          ratified by the state council a land (with an area of 400 hectares) 
          was dedicated by the Forestry Organization to ZNU for the purpose of 
          educational as well as developmental and physical activities. On Feb 
          20, 1976 Zanjan higher school for Agriculture & Animal Science 
          started its work under new title Zanjan Agriculture & Animal Science 
          College while it enhanced it's Agriculture & Animal Science Courses 
          to a BS. Level. Regarding the Council for the Expansion of Higher 
          Education agreement (April 25,1987) on enrolling students in the 
          Engineering college , The council agreed to alter the Zanjan A&AS 
          college title to the center for higher education on May 8, 1987. 
          (Including Engineering and Agriculture colleges). Following this the 
          science and humanities colleges were established in 1990 & 1991, 
          respectively. And lastly the Ministry of Culture and Higher Education 
          agreed to change the center for higher school in Zanjan to Zanjan 
          University.
